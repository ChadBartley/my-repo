console.log( 5 & 1); // 0101 & 0001 => 0001
console.log( 5 | 1); // 0101 | 0001 => 0101
console.log( ~5 ); // 0101 => 1010
console.log( 5 << 1); // 0101 << 0001 => 1010
console.log( 5 ^ 1); // 0101 ^ 0001 => 0100
console.log( 5 >> 1); // 0101 >> 0001 => 0010
console.log( 5 >>> 1); // 0101 >>> 0001 => 0010